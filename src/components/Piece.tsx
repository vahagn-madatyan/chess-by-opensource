/**
 * Chess piece SVG component
 * Renders a chess piece based on type and color
 */
export function Piece({ type, color }: PieceProps) {
  const key = `${color === 'white' ? 'w' : 'b'}${type.charAt(0)}`;
  const pieceData = piecePaths[key];

  if (!pieceData) {
    return null;
  }

  const fill = color === 'white' ? '#f3f4f6' : '#1f2937';
  const stroke = color === 'white' ? '#111827' : '#f3f4f6';

  return (
    <svg
      viewBox={pieceData.viewBox}
      className="w-full h-full"
      aria-label={`${color} ${type}`}
    >
      <path
        d={pieceData.path}
        fill={fill}
        stroke={stroke}
        strokeWidth={1.5}
        strokeLinecap="round"
        strokeLinejoin="round"
      />
    </svg>
  );
}

export default Piece;
